package com.chinarewards.metro.core.common;

import java.io.Serializable;

public class BoxValue<K, V> implements Serializable {

	private static final long serialVersionUID = 1L;

	private K key;
	
	private V value;
	
	public BoxValue() {}
	
	public BoxValue(K key) {
		this.key = key;
	}
	
	public BoxValue(K key, V value) {
		this.key = key;
		this.value = value;
	}

	public K getKey() {
		return key;
	}

	public void setKey(K key) {
		this.key = key;
	}

	public V getValue() {
		return value;
	}

	public void setValue(V value) {
		this.value = value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		BoxValue<?, ?> other = (BoxValue<?, ?>) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}
}