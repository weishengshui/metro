package com.chinarewards.metro.model.common;

/**
 * 图片信息vo
 * @author weishengshui
 *
 */
public class ImageInfo {
	
	private int width;
	private int height;
	public ImageInfo() {
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	
	
}
